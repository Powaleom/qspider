import java.lang.*;

public class evenorodd {

    public static void find(int a)
    {
        if(a %2==0)
        {
            System.out.println("Entered number is even="+a);
        }
        else{
            System.out.println("Enter number is not even="+a);
        }
    }
    public static void main(String[] args) {
        find(3);
    }
    
}
